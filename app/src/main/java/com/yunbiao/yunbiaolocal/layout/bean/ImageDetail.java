package com.yunbiao.yunbiaolocal.layout.bean;

public class ImageDetail {
	private String isAutoPlay;
	private String playTime;
	private String imagePlayType;

	public String getImagePlayType() {
		return imagePlayType;
	}
	public void setImagePlayType(String imagePlayType) {
		this.imagePlayType = imagePlayType;
	}
	public String getIsAutoPlay() {
		return isAutoPlay;
	}
	public void setIsAutoPlay(String isAutoPlay) {
		this.isAutoPlay = isAutoPlay;
	}
	public String getPlayTime() {
		return playTime;
	}
	public void setPlayTime(String playTime) {
		this.playTime = playTime;
	}
}
